func TestParseABI(t *testing.T) {
	a0, err := abi.JSON(strings.NewReader(abiStr))
	assert.NoError(t, err)

	for name, m := range a0.Methods {
		t.Logf("method name: %v\n", name)
		t.Logf("name: %v ", m.Name)
		t.Logf("inputs: %v ", m.Inputs)
		for _, input := range []abi.Argument(m.Inputs) {
			t.Logf("input type: %v type enum: %v stringKind: %v", input.Type, input.Type.T, input.Type.String())
		}
		t.Logf("outputs: %v ", m.Outputs)
		t.Logf("st: %v", m.StateMutability)
		t.Logf("\n")
	}
}
